CREATE OR REPLACE dynamic table CRE_DB.CRE_CORE.CRE_ASP_EVENT_CORE(
	EVENT_ID,
	RESERVATION_ID,
	RESERVATION_STATUS,
	STATUS_REASON,
	RESERVATION_START_AT,
	RESERVATION_END_AT,
	CREATED_AT,
	UPDATED_AT,
	TITLE,
	RESOURCE_ID,
	RESOURCE_TYPE,
	RESOURCE_SUB_TYPE,
	RESOURCE_NETWORK_NAME,
	RESOURCE_FLOOR_NAME,
	RESOURCE_NETWORK_ID,
	RESOURCE_NAME,
	RESOURCE_STATUS,
	USER_ID,
	EMAIL,
	DISPLAY_NAME,
	ATTENDEE_STATUS,
	CREATED_BY,
	CREATED_TIME,
	UPDATED_BY
) target_lag = '5 minutes' refresh_mode = AUTO initialize = ON_CREATE warehouse = CRE_WH_ARCH_ADMIN_XS
 as
SELECT 
    EVENT_ID,
    RESERVATION_ID,
    RESERVATION_STATUS,
    STATUS_REASON,
     case when SNOWFLAKE_TIME_ZONE='America/Los_Angeles' then RESERVATION_START_AT

else CONVERT_TIMEZONE(SNOWFLAKE_TIME_ZONE, 'America/Los_Angeles', RESERVATION_START_AT) end  AS RESERVATION_START_AT,
    case when SNOWFLAKE_TIME_ZONE='America/Los_Angeles' then RESERVATION_END_AT

else CONVERT_TIMEZONE(SNOWFLAKE_TIME_ZONE, 'America/Los_Angeles', RESERVATION_END_AT) end  AS  RESERVATION_END_AT,
         case when SNOWFLAKE_TIME_ZONE='America/Los_Angeles' then CREATED_AT

else CONVERT_TIMEZONE(SNOWFLAKE_TIME_ZONE, 'America/Los_Angeles', CREATED_AT) end  AS  CREATED_AT,
            case when SNOWFLAKE_TIME_ZONE='America/Los_Angeles' then UPDATED_AT

else CONVERT_TIMEZONE(SNOWFLAKE_TIME_ZONE, 'America/Los_Angeles', UPDATED_AT) end  AS  UPDATED_AT,
    TITLE,
    RESOURCE_ID,
    RESOURCE_TYPE,
    RESOURCE_SUB_TYPE,
    RESOURCE_NETWORK_NAME,
    RESOURCE_FLOOR_NAME,
    RESOURCE_NETWORK_ID,
    RESOURCE_NAME,
    RESOURCE_STATUS,
    USER_ID,
    EMAIL,
    DISPLAY_NAME,
    ATTENDEE_STATUS,
    'SYSTEM' AS CREATED_BY,
    CURRENT_TIMESTAMP() AS CREATED_TIME,
    'SYSTEM' AS UPDATED_BY 
FROM CRE_DB.CRE_STG.ASP_EVENT_STG E
INNER JOIN CRE_DB.CRE_CORE.CRE_ASP_TIME_ZONE T
ON E.START_TIME_ZONE=T.ASP_TIME_VALUE
Where E.RESOURCE_TYPE='Space' and E.RESOURCE_SUB_TYPE='Desk'

