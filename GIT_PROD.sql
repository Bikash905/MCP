CREATE OR REPLACE PROCEDURE CRE_DB.CRE_RPT.CRE_SP_ASP_PROCESS_ALL_BUILDINGS()
RETURNS VARCHAR
LANGUAGE PYTHON
RUNTIME_VERSION = '3.11'
PACKAGES = ('snowflake-snowpark-python','requests')
HANDLER = 'process_building'
EXECUTE AS OWNER
AS '
from snowflake.snowpark import Session
def process_building(session: Session):
    try:
        # Get DISTINCT building names Reservation kpi table
        buildings_df = session.table("CRE_DB.CRE_RPT.CRE_ASP_RESERVATION_KPI") \\
                             .select("RESOURCE_NETWORK_NAME").distinct()
        buildings = buildings_df.collect()
        success_count = 0
        failure_count = 0
        error_messages = []
        for row in buildings:
            building_name = row["RESOURCE_NETWORK_NAME"]
            try:
                # Modified query to use building_name from ASP core table
                query = f"""
                    INSERT INTO CRE_DB.CRE_RPT.CRE_WORKSPACE_RESERVATION_TAB
                    SELECT 
                        CC.EVENT_ID, 
                        CC.RESERVATION_ID, 
                        CC.RESERVATION_STATUS,
                        CC.RESERVATION_START_AT, 
                        CC.RESERVATION_END_AT,
                        CC.RESOURCE_TYPE,
                        CC.RESOURCE_SUB_TYPE, 
                        CC.RESOURCE_NETWORK_NAME AS RESERVATION_BUILDING_NAME ,
                        CC.RESOURCE_FLOOR_NAME,
                        CC.RESOURCE_NAME,
                        CC.EMAIL ,
	                    CC.DISPLAY_NAME AS ATTENDEE_NAME ,
                        CC.L3_NAME,
                        CC.PERNR,
                        DD.ASSIGNED_HEAD_COUNT,
                        DD.CAPACITY,
                        CC.ASSIGNED_RESERVATION
                    FROM CRE_DB.CRE_RPT.CRE_ASP_RESERVATION_KPI CC
                    LEFT JOIN (
                        SELECT * 
                        FROM CRE_DB.CRE_RPT.CRE_SU_NTH_EDH_TRR_ASP_KPI
                        WHERE WORK_LOCATION_CODE IN (
                            SELECT SAP_BUILDING_ID 
                            FROM CRE_DB.CRE_CORE.VW_CRE_BUILDING_LOOKUP 
                            WHERE APPSPACE_BUILDING_ID = ''{building_name}''
                        )
                    ) DD
                    ON CC.L3_NAME = DD.L3_NAME
                    WHERE CC.RESOURCE_NETWORK_NAME = ''{building_name}'';
                """
                session.sql(query).collect()
                print(f"Processed building: {building_name}")
                success_count += 1
            except Exception as e:
                error_msg = f"Failed {building_name}: {str(e)}"
                print(error_msg)
                error_messages.append(error_msg)
                failure_count += 1
        result = f"Success: {success_count}, Failures: {failure_count}"
        if failure_count > 0:
            result += f"\\nErrors: {'', ''.join(error_messages[:5])}"  # Show first 5 errors
        return result
    except Exception as e:
        return f"Critical error: {str(e)}"
def run(session: Session) -> str:
    return process_building(session)
';
