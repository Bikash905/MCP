CREATE OR REPLACE TASK CRE_DB.CRE_RPT.CRE_TSK_SU_NTH_EDH_TRR_ASP_KPI
	warehouse=CRE_WH_ARCH_ADMIN_XS
	schedule='USING CRON 30 3 * * * America/Los_Angeles'
	as BEGIN
    TRUNCATE TABLE CRE_DB.CRE_RPT.CRE_SU_NTH_EDH_TRR_ASP_KPI;
	
	INSERT INTO CRE_DB.CRE_RPT.CRE_SU_NTH_EDH_TRR_ASP_KPI
SELECT L3_NAME, L3_PERNR,LENEL_BUILDING_ID,APPSPACE_BUILDING_ID, WORK_LOCATION_CODE,
COUNT(DISTINCT PERNR) AS ASSIGNED_HEAD_COUNT,
CAPACITY,COALESCE(COUNT_CORPORATE_UNASSIGNED, 0) AS COUNT_CORPORATE_UNASSIGNED,CURRENT_USER() AS CREATED_BY,CURRENT_TIMESTAMP() AS CREATED_TIME,CURRENT_USER() AS UPDATED_BY FROM
CRE_DB.CRE_RPT.CRE_EDH_LEADER_NTH_HIER AA
LEFT JOIN
(SELECT SPACE_DEPARTMENT_LEADER_PERNR,SPACE_DEPARTMENT_LEADER,SAP_BUILDING_ID,LENEL_BUILDING_ID,APPSPACE_BUILDING_ID,
SUM(CASE WHEN SPACE_DEPARTMENT_LEADER <> '' THEN CAPACITY END) AS CAPACITY,
SUM(CASE WHEN SPACE_DEPARTMENT_LEADER = '' THEN CAPACITY END) AS COUNT_CORPORATE_UNASSIGNED
FROM
CRE_DB.CRE_CORE.CRE_TRR_PBFS_CORE
WHERE UPPER(SPACE_STATUS) <> 'RETIRED'
GROUP BY
SPACE_DEPARTMENT_LEADER_PERNR,
SPACE_DEPARTMENT_LEADER,
SAP_BUILDING_ID,
LENEL_BUILDING_ID,APPSPACE_BUILDING_ID) BB
ON
AA.L3_PERNR = BB.SPACE_DEPARTMENT_LEADER_PERNR
AND AA.WORK_LOCATION_CODE = BB.SAP_BUILDING_ID
WHERE UPPER(AA.EMPLOYMENT_STATUS_DESCRIPTION) IN ('ACTIVE')
GROUP BY
L3_NAME,
L3_PERNR,
LENEL_BUILDING_ID,
APPSPACE_BUILDING_ID,
WORK_LOCATION_CODE,
CAPACITY,
COALESCE(COUNT_CORPORATE_UNASSIGNED, 0);
END;
